#!/bin/bash
echo "Shows disk space usage of file systems in a human-readable format : ps"
ps
echo "Shows disk space usage of file systems in a human-readable format : df -h"
df -h
echo "Displays RAM and swap memory usage in MB. (Linux only) : free -m"
free -m
